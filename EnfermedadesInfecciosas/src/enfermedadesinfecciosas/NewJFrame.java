     public Icon seticono(String url, JButton boton){
        ImageIcon iconn = new ImageIcon(getClass().getResource(url));
        int ancho = boton.getWidth();
        int alto = boton.getHeight();
        ImageIcon icon1 = new ImageIcon(iconn.getImage().getScaledInstance(ancho, alto, Image.SCALE_DEFAULT));
        return icon1;
    }
     
    public Icon setIconoPresionado(String url, JButton boton, int ancho, int alto){
        ImageIcon iconn = new ImageIcon(getClass().getResource(url));
        int wit = boton.getWidth() -ancho;
        int ht = boton.getHeight() -alto;
        ImageIcon icon1 = new ImageIcon(iconn.getImage().getScaledInstance(wit, ht, Image.SCALE_DEFAULT));
        return icon1;
    }
    
